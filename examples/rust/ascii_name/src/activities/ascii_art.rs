//! ASCII Art character definitions

/// Returns the ASCII art representation of a character (7 lines tall)
pub fn get_ascii_art_char(c: char) -> Vec<&'static str> {
    match c {
        // Uppercase letters
        'A' => vec![
            "    _    ",
            "   / \\   ",
            "  / _ \\  ",
            " / ___ \\ ",
            "/_/   \\_\\",
            "         ",
            "         ",
        ],
        'B' => vec![
            " ____  ", "| __ ) ", "|  _ \\ ", "| |_) |", "|____/ ", "       ", "       ",
        ],
        'C' => vec![
            "  ____ ", " / ___|", "| |    ", "| |___ ", " \\____|", "       ", "       ",
        ],
        'D' => vec![
            " ____  ", "|  _ \\ ", "| | | |", "| |_| |", "|____/ ", "       ", "       ",
        ],
        'E' => vec![
            " _____ ", "| ____|", "|  _|  ", "| |___ ", "|_____|", "       ", "       ",
        ],
        'F' => vec![
            " _____ ", "| ____|", "|  _|  ", "| |    ", "|_|    ", "       ", "       ",
        ],
        'G' => vec![
            "  ____ ", " / ___|", "| |  _ ", "| |_| |", " \\____|", "       ", "       ",
        ],
        'H' => vec![
            " _   _ ", "| | | |", "| |_| |", "|  _  |", "|_| |_|", "       ", "       ",
        ],
        'I' => vec![
            " ___ ", "|_ _|", " | | ", " | | ", "|___|", "     ", "     ",
        ],
        'J' => vec![
            "     _ ", "    | |", " _  | |", "| |_| |", " \\___/ ", "       ", "       ",
        ],
        'K' => vec![
            " _  __", "| |/ /", "| ' / ", "| . \\ ", "|_|\\_\\", "      ", "      ",
        ],
        'L' => vec![
            " _     ", "| |    ", "| |    ", "| |___ ", "|_____|", "       ", "       ",
        ],
        'M' => vec![
            " __  __ ",
            "|  \\/  |",
            "| |\\/| |",
            "| |  | |",
            "|_|  |_|",
            "        ",
            "        ",
        ],
        'N' => vec![
            " _   _ ", "| \\ | |", "|  \\| |", "| |\\  |", "|_| \\_|", "       ", "       ",
        ],
        'O' => vec![
            "  ___  ", " / _ \\ ", "| | | |", "| |_| |", " \\___/ ", "       ", "       ",
        ],
        'P' => vec![
            " ____  ", "|  _ \\ ", "| |_) |", "|  __/ ", "|_|    ", "       ", "       ",
        ],
        'Q' => vec![
            "  ___  ",
            " / _ \\ ",
            "| | | |",
            "| |_| |",
            " \\__\\_\\",
            "       ",
            "       ",
        ],
        'R' => vec![
            " ____  ",
            "|  _ \\ ",
            "| |_) |",
            "|  _ < ",
            "|_| \\_\\",
            "       ",
            "       ",
        ],
        'S' => vec![
            " ____  ",
            "/ ___| ",
            "\\___ \\ ",
            " ___) |",
            "|____/ ",
            "       ",
            "       ",
        ],
        'T' => vec![
            " _____ ", "|_   _|", "  | |  ", "  | |  ", "  |_|  ", "       ", "       ",
        ],
        'U' => vec![
            " _   _ ", "| | | |", "| | | |", "| |_| |", " \\___/ ", "       ", "       ",
        ],
        'V' => vec![
            "__     __",
            "\\ \\   / /",
            " \\ \\ / / ",
            "  \\ V /  ",
            "   \\_/   ",
            "         ",
            "         ",
        ],
        'W' => vec![
            "__        __",
            "\\ \\      / /",
            " \\ \\ /\\ / / ",
            "  \\ V  V /  ",
            "   \\_/\\_/   ",
            "            ",
            "            ",
        ],
        'X' => vec![
            "__  __", "\\ \\/ /", " \\  / ", " /  \\ ", "/_/\\_\\", "      ", "      ",
        ],
        'Y' => vec![
            "__   __",
            "\\ \\ / /",
            " \\ V / ",
            "  | |  ",
            "  |_|  ",
            "       ",
            "       ",
        ],
        'Z' => vec![
            " _____", "|__  /", "  / / ", " / /_ ", "/____|", "      ", "      ",
        ],

        // Lowercase letters
        'a' => vec![
            "       ", "  __ _ ", " / _` |", "| (_| |", " \\__,_|", "       ", "       ",
        ],
        'b' => vec![
            " _     ", "| |__  ", "| '_ \\ ", "| |_) |", "|_.__/ ", "       ", "       ",
        ],
        'c' => vec![
            "      ", "  ___ ", " / __|", "| (__ ", " \\___|", "      ", "      ",
        ],
        'd' => vec![
            "     _ ", "  __| |", " / _` |", "| (_| |", " \\__,_|", "       ", "       ",
        ],
        'e' => vec![
            "      ", "  ___ ", " / _ \\", "|  __/", " \\___|", "      ", "      ",
        ],
        'f' => vec![
            "  __ ", " / _|", "| |_ ", "|  _|", "|_|  ", "     ", "     ",
        ],
        'g' => vec![
            "       ", "  __ _ ", " / _` |", "| (_| |", " \\__, |", " |___/ ", "       ",
        ],
        'h' => vec![
            " _     ", "| |__  ", "| '_ \\ ", "| | | |", "|_| |_|", "       ", "       ",
        ],
        'i' => vec![" _ ", "(_)", "| |", "| |", "|_|", "   ", "   "],
        'j' => vec![
            "   _ ", "  (_)", "  | |", "  | |", " _/ |", "|__/ ", "     ",
        ],
        'k' => vec![
            " _    ", "| | __", "| |/ /", "|   < ", "|_|\\_\\", "      ", "      ",
        ],
        'l' => vec![" _ ", "| |", "| |", "| |", "|_|", "   ", "   "],
        'm' => vec![
            "           ",
            " _ __ ___  ",
            "| '_ ` _ \\ ",
            "| | | | | |",
            "|_| |_| |_|",
            "           ",
            "           ",
        ],
        'n' => vec![
            "        ",
            " _ __   ",
            "| '_ \\  ",
            "| | | | ",
            "|_| |_| ",
            "        ",
            "        ",
        ],
        'o' => vec![
            "       ", "  ___  ", " / _ \\ ", "| (_) |", " \\___/ ", "       ", "       ",
        ],
        'p' => vec![
            "       ", " _ __  ", "| '_ \\ ", "| |_) |", "| .__/ ", "| |    ", "|_|    ",
        ],
        'q' => vec![
            "       ", "  __ _ ", " / _` |", "| (_| |", " \\__, |", "    | |", "    |_|",
        ],
        'r' => vec![
            "      ", " _ __ ", "| '__|", "| |   ", "|_|   ", "      ", "      ",
        ],
        's' => vec![
            "     ", " ___ ", "/ __|", "\\__ \\", "|___/", "     ", "     ",
        ],
        't' => vec![
            " _   ", "| |_ ", "| __|", "| |_ ", " \\__|", "     ", "     ",
        ],
        'u' => vec![
            "        ",
            " _   _  ",
            "| | | | ",
            "| |_| | ",
            " \\__,_| ",
            "        ",
            "        ",
        ],
        'v' => vec![
            "        ",
            "__   __ ",
            "\\ \\ / / ",
            " \\ V /  ",
            "  \\_/   ",
            "        ",
            "        ",
        ],
        'w' => vec![
            "           ",
            "__      __ ",
            "\\ \\ /\\ / / ",
            " \\ V  V /  ",
            "  \\_/\\_/   ",
            "           ",
            "           ",
        ],
        'x' => vec![
            "       ",
            "__  __ ",
            "\\ \\/ / ",
            " >  <  ",
            "/_/\\_\\ ",
            "       ",
            "       ",
        ],
        'y' => vec![
            "        ",
            " _   _  ",
            "| | | | ",
            "| |_| | ",
            " \\__, | ",
            " |___/  ",
            "        ",
        ],
        'z' => vec![
            "      ", " ____ ", "|_  / ", " / /  ", "/___| ", "      ", "      ",
        ],

        // Special characters
        '.' => vec!["   ", "   ", "   ", "   ", " _ ", "(_)", "   "],
        ',' => vec!["   ", "   ", "   ", "   ", " _ ", "( )", "|/ "],
        '!' => vec![" _ ", "| |", "| |", "|_|", "(_)", "   ", "   "],
        '?' => vec![
            " ___ ", "|__ \\", "  / /", " |_| ", " (_) ", "     ", "     ",
        ],
        ':' => vec!["   ", " _ ", "(_)", " _ ", "(_)", "   ", "   "],
        ';' => vec!["   ", " _ ", "(_)", " _ ", "( )", "|/ ", "   "],
        '\'' => vec![" _ ", "( )", "|/ ", "   ", "   ", "   ", "   "],
        '"' => vec![
            " _ _ ", "( | )", " V V ", "     ", "     ", "     ", "     ",
        ],
        '-' => vec![
            "      ", "      ", " ____ ", "|____|", "      ", "      ", "      ",
        ],
        '_' => vec![
            "      ", "      ", "      ", "      ", " ____ ", "|____|", "      ",
        ],
        '+' => vec![
            "       ", "   _   ", " _| |_ ", "|_   _|", "  |_|  ", "       ", "       ",
        ],
        '=' => vec![
            "      ", "      ", " ____ ", "|____|", "|____|", "      ", "      ",
        ],
        '/' => vec![
            "    __", "   / /", "  / / ", " / /  ", "/_/   ", "      ", "      ",
        ],
        '\\' => vec![
            "__    ", "\\ \\   ", " \\ \\  ", "  \\ \\ ", "   \\_\\", "      ", "      ",
        ],
        '(' => vec!["  __", " / /", "| | ", "| | ", "| | ", " \\_\\", "    "],
        ')' => vec!["__  ", "\\ \\ ", " | |", " | |", " | |", "/_/ ", "    "],
        '[' => vec![" ___", "| | ", "| | ", "| | ", "| | ", "|_|_ ", "    "],
        ']' => vec!["___ ", " | |", " | |", " | |", " | |", "_|_|", "    "],
        '{' => vec!["  __", " / /", "| | ", "< < ", "| | ", " \\_\\", "    "],
        '}' => vec!["__  ", "\\ \\ ", " | |", " > >", " | |", "/_/ ", "    "],
        '@' => vec![
            "   ____  ",
            "  / __ \\ ",
            " / / _` |",
            "| | (_| |",
            " \\ \\__,_|",
            "  \\____/ ",
            "          ",
        ],
        '#' => vec![
            "   _  _   ",
            " _| || |_ ",
            "|_  __  _|",
            " _| || |_ ",
            "|_  __  _|",
            "  |_||_|  ",
            "          ",
        ],
        '$' => vec![
            "  _  ", " | | ", "/ __)", "\\__ \\", "(   /", " |_| ", "     ",
        ],
        '%' => vec![
            " _  __", "(_)/ /", "  / / ", " / /  ", "/_/(_)", "      ", "      ",
        ],
        '^' => vec![" /\\ ", "|/\\|", "    ", "    ", "    ", "    ", "    "],
        '&' => vec![
            "  ___   ",
            " ( _ )  ",
            " / _ \\/\\",
            "| (_>  <",
            " \\___/\\/",
            "        ",
            "        ",
        ],
        '*' => vec![
            "      ", "\\  /  ", " >< _ ", "/_/\\_\\", "      ", "      ", "      ",
        ],
        ' ' => vec!["    ", "    ", "    ", "    ", "    ", "    ", "    "],
        // Default case for unsupported characters
        _ => vec![" ? ", " ? ", " ? ", " ? ", " ? ", " ? ", " ? "],
    }
}
